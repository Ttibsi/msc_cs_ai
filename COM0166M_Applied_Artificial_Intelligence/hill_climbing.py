import random
import time

type matrix_t = dict[int, list[int]]


def create_matrix() -> matrix_t:
    lines = data.strip().split("\n")
    matrix = {idx: x.split(",") for idx, x in enumerate(lines)}
    for k, v in matrix.items():
        matrix[k] = [int(x) for x in v]
    return matrix


def greedy_initial(matrix: matrix_t, start_point: int) -> dict[int, int]:
    ret: dict[int, int] = {start_point:0}
    remaining: list[int] = list(range(0,50))
    remaining.remove(start_point)

    while remaining:
        ret[remaining[0]] = 0
        if len(remaining) == 1:
            # As of python3.7, dictionaries preserve insertion order.
            prev_key = list(ret.keys())[-1]
            cost = matrix[prev_key][remaining[0]]
            ret[remaining[0]] = cost
            break

        index = remaining[0]
        min_dist = 1000
        min_elem = 55
        for k, v in matrix.items():
            if k in ret:
                continue

            if v[index] == 0:
                continue

            if v[index] < min_dist:
                min_dist = v[index]
                min_elem = k

        remaining.remove(min_elem)
        ret[min_elem] = min_dist

    return ret


def swap_in_place(path: dict[int, int], key_1: int, key_2: int) -> list[int]:
    path[key_1], path[key_2] = path[key_2], path[key_1]
    return path


def hill_walk(matrix: matrix_t, path: dict[int, int]) -> dict[int, int]:
    # Don't swap the last element, or the start point
    swap_index = random.randint(1, 48)
    prev = list(path.keys())[swap_index - 1]
    first = list(path.keys())[swap_index]
    second = list(path.keys())[swap_index + 1]

    path[second] = matrix[prev][second]
    path[first] = matrix[second][first]

    return path


def path_cost(path: dict[int, int], matrix: matrix_t) -> int:
    final_elem = list(path.keys())[-1]
    first_elem = list(path.keys())[0]
    return sum(path.values()) + matrix[final_elem][first_elem]


def main() -> int:
    matrix = create_matrix()

    print("\u250C" + ("\u2500" * 64) + "\u2510")
    for i in range(0, 10):
        start = random.randint(0, 49)
        start_time = time.perf_counter_ns()

        tsp_path = greedy_initial(matrix, start)
        cost = path_cost(tsp_path, matrix)

        no_improvements = 0
        while True:
            tsp_path = hill_walk(matrix, tsp_path)
            new_cost = path_cost(tsp_path, matrix)
            if new_cost >= cost:
                no_improvements += 1

                if no_improvements == 25:
                    break
            else:
                cost = new_cost
                no_improvements = 0

        total_time = time.perf_counter_ns() - start_time
        print(f"\u2502 {i} \u2502 Starting element: {start:0<2} \u2502 Total cost: {cost} \u2502 Time (ns): {total_time} \u2502")

    print("\u2514" + ("\u2500" * 64) + "\u2518")
    return 0

if __name__ == "__main__":
    raise SystemExit(main())
